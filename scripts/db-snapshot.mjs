// DB 스냅샷/복원 도구 — 테스트로 바뀐 데이터를 깨끗한 seed로 되돌린다.
//   npm run db:backup  : 현재 5개 테이블을 bak_* 테이블로 백업(사진 BLOB 포함)
//   npm run db:reset   : 백업 시점으로 5개 테이블을 원상복구
// 실행: node --env-file=.env.local scripts/db-snapshot.mjs <backup|reset>
import oracledb from "oracledb";

oracledb.outFormat = oracledb.OUT_FORMAT_OBJECT;

const TABLES = ["customer", "item", "purchasereq", "chatroom", "message"];
// FK 때문에 삭제는 자식→부모, 삽입은 부모→자식 순서로 한다.
const DELETE_ORDER = ["message", "chatroom", "purchasereq", "item", "customer"];
const INSERT_ORDER = ["customer", "item", "purchasereq", "chatroom", "message"];

async function tableExists(conn, name) {
  const r = await conn.execute(
    `SELECT COUNT(*) AS n FROM user_tables WHERE table_name = :n`,
    { n: name.toUpperCase() }
  );
  return r.rows[0].N > 0;
}

async function counts(conn) {
  const out = {};
  for (const t of TABLES) {
    const r = await conn.execute(`SELECT COUNT(*) AS n FROM ${t}`);
    out[t] = r.rows[0].N;
  }
  return out;
}

async function backup(conn) {
  for (const t of TABLES) {
    const bak = `bak_${t}`;
    if (await tableExists(conn, bak)) await conn.execute(`DROP TABLE ${bak} PURGE`);
    // CTAS: 데이터(사진 BLOB 포함)를 그대로 복사. identity 속성은 복사되지 않아 일반 컬럼이 된다.
    await conn.execute(`CREATE TABLE ${bak} AS SELECT * FROM ${t}`);
    console.log(`  ✓ ${t} → ${bak}`);
  }
  console.log("백업 완료. 이제 마음껏 테스트하고 'npm run db:reset' 으로 되돌리세요.");
}

async function reset(conn) {
  for (const t of TABLES) {
    if (!(await tableExists(conn, `bak_${t}`))) {
      console.error(
        `백업 테이블 bak_${t} 이(가) 없습니다. 먼저 'npm run db:backup' 을 실행하세요.`
      );
      process.exit(1);
    }
  }

  // identity(roomNo/seqNo)가 GENERATED ALWAYS라 명시적 값 삽입이 막혀 있다.
  // 잠시 BY DEFAULT로 바꿔 백업의 원래 값을 그대로 되살린다.
  await conn.execute(
    `ALTER TABLE chatroom MODIFY (roomNo GENERATED BY DEFAULT AS IDENTITY)`
  );
  await conn.execute(
    `ALTER TABLE message MODIFY (seqNo GENERATED BY DEFAULT AS IDENTITY)`
  );

  for (const t of DELETE_ORDER) await conn.execute(`DELETE FROM ${t}`);
  for (const t of INSERT_ORDER) {
    await conn.execute(`INSERT INTO ${t} SELECT * FROM bak_${t}`);
  }
  await conn.commit();

  // 예약 중 물품의 예약시각(resDateTime)을 '지금'으로 갱신한다.
  // 백업의 예약시각이 과거라, 복원 직후 화면 진입 시 48시간 자동취소(단계 8)에
  // 곧바로 사라지는 것을 막기 위함. → reset 직후에는 항상 신선한 '예약 중' 물품이 존재.
  await conn.execute(
    `UPDATE item SET resDateTime = LOCALTIMESTAMP WHERE sellStatus = :reserved`,
    { reserved: "예약 중" }
  );
  await conn.commit();

  // identity 카운터를 현재 최대값 다음으로 맞춘다(이후 앱이 새로 만드는 번호 충돌 방지).
  // 가능하면 원래대로 GENERATED ALWAYS 로 되돌린다(실패해도 앱 동작엔 영향 없음).
  for (const [tbl, col] of [
    ["chatroom", "roomNo"],
    ["message", "seqNo"],
  ]) {
    try {
      await conn.execute(
        `ALTER TABLE ${tbl} MODIFY (${col} GENERATED ALWAYS AS IDENTITY START WITH LIMIT VALUE)`
      );
    } catch {
      await conn.execute(
        `ALTER TABLE ${tbl} MODIFY (${col} GENERATED BY DEFAULT AS IDENTITY START WITH LIMIT VALUE)`
      );
    }
  }

  console.log("복원 완료:", JSON.stringify(await counts(conn)));
}

async function main() {
  const cmd = process.argv[2];
  if (cmd !== "backup" && cmd !== "reset") {
    console.error("사용법: node scripts/db-snapshot.mjs <backup|reset>");
    process.exit(1);
  }
  const conn = await oracledb.getConnection({
    user: process.env.DB_USER,
    password: process.env.DB_PASSWORD,
    connectString: process.env.DB_CONNECT_STRING,
  });
  try {
    if (cmd === "backup") await backup(conn);
    else await reset(conn);
  } finally {
    await conn.close();
  }
}

main().catch((e) => {
  console.error("오류:", e.message);
  process.exit(1);
});
